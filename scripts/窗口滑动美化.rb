#==============================================================================
# ■ 下面Scene_Base部分最好不要动，新场景写在最下方
#==============================================================================
class Scene_Base
  #--------------------------------------------------------------------------
  # ● 开始后处理
  #--------------------------------------------------------------------------
  alias vip_slide_post_start post_start
  def post_start
    init_slide
    vip_slide_post_start
    slide_start
  end
  #--------------------------------------------------------------------------
  # ● 滑动前的准备
  #--------------------------------------------------------------------------
  def init_slide;end
  #--------------------------------------------------------------------------
  # ● 窗口滑动处理
  #--------------------------------------------------------------------------
  def slide_start;end
end
#-------------------------------------------------------------------------------
# ■ 上面部分最好不要改
#==============================================================================
# 　菜单画面
#==============================================================================
class Scene_Menu < Scene_MenuBase
  #--------------------------------------------------------------------------
  # ● 滑动前的准备
  # 重定义开始时各个窗口的位置（基本会设置到屏幕外），
  # 具体有哪些窗口去各个场景里面自己找。
  #--------------------------------------------------------------------------
  def init_slide
    @command_window.y -= 150
    @gold_window.x -= 100
    @status_window.x = 260
  end
  #--------------------------------------------------------------------------
  # ● 窗口滑动处理
  #--------------------------------------------------------------------------
  def slide_start
    10.times do
      @command_window.y += 15
      @gold_window.x    += 10
      @status_window.x  -= 10
                      # 每帧移动的坐标量
      Graphics.update # 刷新窗口
    end
    10.times{|i|
    @gold_window.x    += 5 * Math.cos(i)
    @command_window.y += 5 * Math.cos(i)
    Graphics.update}  # 弹动
  end
end
#==============================================================================
# 　物品画面
#==============================================================================
class Scene_Item < Scene_ItemBase
  #--------------------------------------------------------------------------
  # ● 滑动前的准备
  #--------------------------------------------------------------------------
  def init_slide
    @category_window.x -= 100
    @item_window.y += 100
    @help_window.y -= 60
  end
  #--------------------------------------------------------------------------
  # ● 窗口滑动处理
  #--------------------------------------------------------------------------
  def slide_start
    10.times do
      @category_window.x += 10
      @item_window.y -= 10
      @help_window.y += 6
      # 每帧移动的坐标量
      Graphics.update # 刷新窗口
    end
  end
end
